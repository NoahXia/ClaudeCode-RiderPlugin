package com.anthropic.claudecode.rider.process

import com.anthropic.claudecode.rider.browser.ClaudeBrowserManager
import com.anthropic.claudecode.rider.toolwindow.ClaudeIconManager
import com.anthropic.claudecode.rider.toolwindow.ClaudeToolWindowPanel
import com.intellij.notification.NotificationGroupManager
import com.intellij.notification.NotificationType
import com.intellij.openapi.Disposable
import com.intellij.openapi.application.ApplicationManager
import com.intellij.openapi.components.Service
import com.intellij.openapi.components.service
import com.intellij.openapi.diagnostic.Logger
import com.intellij.openapi.project.Project
import com.intellij.openapi.wm.ToolWindowManager
import kotlinx.coroutines.*
import kotlinx.serialization.json.*
import java.io.File
import java.io.IOException
import java.util.concurrent.ConcurrentHashMap

/**
 * Project-level service managing Claude CLI subprocesses.
 *
 * Each webview "channel" (conversation) maps to one spawned claude process.
 * Messages flow as JSON lines on stdin/stdout following the Claude Code SDK protocol.
 */
@Service(Service.Level.PROJECT)
class ClaudeProcessManager(private val project: Project) : Disposable {

    companion object {
        fun getInstance(project: Project): ClaudeProcessManager = project.service()
    }

    private val log = Logger.getInstance(ClaudeProcessManager::class.java)
    private val scope = CoroutineScope(SupervisorJob() + Dispatchers.IO)

    private data class Channel(val process: Process, val job: Job)
    private val channels = ConcurrentHashMap<String, Channel>()

    /** Channel IDs that were closed on purpose (Stop button, close_channel, dispose).
     *  Processes exiting from this set are never treated as unexpected crashes. */
    private val intentionallyClosed = ConcurrentHashMap.newKeySet<String>()

    /**
     * Tracks in-flight tool permission requests bridged from Claude's control_request
     * to the webview's tool_permission_request RPC flow.
     * Key = webview requestId generated by us; Value = (channelId, Claude's control request_id).
     */
    private val pendingPermissions = ConcurrentHashMap<String, Pair<String, String>>()

    // ── Public API ────────────────────────────────────────────────────────────

    /**
     * Spawns a new Claude process for [channelId].
     * [resume] is an optional session ID to resume an existing conversation.
     */
    fun launchChannel(
        channelId: String,
        cwd: String,
        resume: String?,
        permissionMode: String,
        thinkingLevel: String
    ) {
        if (channels.containsKey(channelId)) {
            log.warn("Channel $channelId already exists, ignoring launch_claude")
            return
        }

        val binary = ClaudeProcessConfig.resolveBinaryPath()
        if (binary == null) {
            log.warn("Cannot launch channel $channelId: claude binary not found")
            sendErrorToWebview(channelId, "Claude CLI not found. Please install claude and configure the path in Settings → Tools → Claude Code.")
            return
        }

        val args = mutableListOf(
            binary,
            "--output-format", "stream-json",
            "--verbose",
            "--input-format", "stream-json",
            "--include-partial-messages"
        )
        // Only use stdio permission prompt when we need interactive permission bridging.
        // acceptEdits / bypassPermissions handle permissions internally in the CLI itself.
        val needsPermissionBridge = permissionMode !in setOf("acceptEdits", "bypassPermissions")
        if (needsPermissionBridge) args += listOf("--permission-prompt-tool", "stdio")
        if (!resume.isNullOrBlank()) args += listOf("--resume", resume)
        if (permissionMode.isNotBlank() && permissionMode != "default")
            args += listOf("--permission-mode", permissionMode)
        log.info("Claude channel $channelId args: ${args.drop(1).joinToString(" ")}")

        try {
            val pb = ProcessBuilder(args)
                .directory(File(cwd.takeIf { File(it).exists() } ?: (project.basePath ?: System.getProperty("user.home"))))
                .redirectErrorStream(false)
            pb.environment().putAll(ClaudeProcessConfig.buildEnvironment())

            val proc = pb.start()
            log.info("Claude channel $channelId started (pid=${proc.pid()}, cwd=$cwd)")

            val job = scope.launch {
                try {
                    // Drain stderr to IDE log (warn level so it's always visible)
                    launch {
                        try {
                            proc.errorStream.bufferedReader(Charsets.UTF_8).forEachLine { line ->
                                log.warn("[claude/$channelId stderr] $line")
                            }
                        } catch (e: IOException) { /* ignore */ }
                    }

                    // Read stdout lines and forward as io_message to webview
                    proc.inputStream.bufferedReader(Charsets.UTF_8).forEachLine { line ->
                        if (line.isNotBlank()) {
                            log.debug("[claude/$channelId stdout] $line")
                            forwardIoMessage(channelId, line)
                        }
                    }
                } catch (e: IOException) {
                    log.debug("Channel $channelId stdout ended: ${e.message}")
                } finally {
                    val exitCode = try { proc.exitValue() } catch (_: IllegalThreadStateException) { -1 }
                    val wasIntentional = intentionallyClosed.remove(channelId)
                    log.info("Claude channel $channelId process exited (code=$exitCode intentional=$wasIntentional)")
                    channels.remove(channelId)
                    sendCloseChannel(channelId, null)
                    ClaudeIconManager.setDone(project)
                    if (exitCode != 0 && !wasIntentional) {
                        ApplicationManager.getApplication().invokeLater {
                            NotificationGroupManager.getInstance()
                                .getNotificationGroup("Claude Code")
                                ?.createNotification(
                                    "Claude process exited unexpectedly (code $exitCode)",
                                    NotificationType.WARNING
                                )
                                ?.notify(project)
                        }
                    }
                }
            }

            channels[channelId] = Channel(proc, job)

        } catch (e: Exception) {
            log.error("Failed to launch channel $channelId", e)
            sendErrorToWebview(channelId, "Failed to start Claude: ${e.message}")
        }
    }

    /**
     * Sends an io_message.message JSON string to the Claude process on [channelId] stdin.
     * [done] signals that no more input is coming (closes stdin).
     */
    fun sendIoMessage(channelId: String, messageJson: String, done: Boolean) {
        val channel = channels[channelId]
        if (channel == null) {
            log.warn("sendIoMessage: channel $channelId not found")
            return
        }
        try {
            val msgType = runCatching { Json.parseToJsonElement(messageJson).jsonObject["type"]?.jsonPrimitive?.contentOrNull }.getOrNull()
            log.info("sendIoMessage: channel=$channelId type=$msgType done=$done len=${messageJson.length}")
            val line = messageJson + "\n"
            channel.process.outputStream.write(line.toByteArray(Charsets.UTF_8))
            channel.process.outputStream.flush()
            if (done) channel.process.outputStream.close()
        } catch (e: IOException) {
            log.warn("Failed to write to channel $channelId stdin", e)
        }
    }

    /** Kills the process for [channelId] and removes it from the map. */
    fun closeChannel(channelId: String) {
        intentionallyClosed.add(channelId)
        val channel = channels.remove(channelId) ?: return
        channel.job.cancel()
        channel.process.destroy()
        log.info("Channel $channelId closed")
    }

    /** Sends SIGINT / destroyForcibly to interrupt the running Claude operation. */
    fun interruptChannel(channelId: String) {
        intentionallyClosed.add(channelId)
        val channel = channels[channelId] ?: return
        try {
            // Mirror VS Code extension: send a control_request with subtype "interrupt" to stdin.
            // Claude handles it gracefully, finishes the turn, then exits with code 0.
            val requestId = java.util.UUID.randomUUID().toString()
            val msg = """{"type":"control_request","request_id":"$requestId","request":{"subtype":"interrupt"}}""" + "\n"
            channel.process.outputStream.write(msg.toByteArray(Charsets.UTF_8))
            channel.process.outputStream.flush()
            log.info("Sent interrupt control_request to channel $channelId")
        } catch (e: Exception) {
            log.warn("Failed to send interrupt to channel $channelId stdin, force-killing: ${e.message}")
            channel.process.destroyForcibly()
        }
    }

    // ── Webview forwarding ────────────────────────────────────────────────────

    private fun forwardIoMessage(channelId: String, line: String) {
        val parsedMessage = runCatching { Json.parseToJsonElement(line).jsonObject }.getOrNull() ?: return

        val msgType = parsedMessage["type"]?.jsonPrimitive?.contentOrNull
        log.info("[channel/$channelId] msg type=$msgType")

        // Log detail for result/error messages to diagnose tool failures
        if (msgType == "result" || msgType == "tool_result") {
            log.info("[channel/$channelId] $msgType detail: ${parsedMessage.toString().take(800)}")
        }
        if (parsedMessage["is_error"]?.jsonPrimitive?.booleanOrNull == true) {
            log.warn("[channel/$channelId] error message: ${parsedMessage.toString().take(800)}")
        }

        if (msgType == "control_request") {
            val subtype = parsedMessage["request"]?.jsonObject?.get("subtype")?.jsonPrimitive?.contentOrNull
            val toolName = parsedMessage["request"]?.jsonObject?.get("tool_name")?.jsonPrimitive?.contentOrNull
            log.info("[channel/$channelId] control_request subtype=$subtype tool=$toolName")
            bridgeControlRequest(channelId, parsedMessage)
            return
        }

        val inner = buildJsonObject {
            put("type", "io_message")
            put("channelId", channelId)
            put("message", parsedMessage)
            put("done", false)
        }
        sendToWebview(inner.toString())
    }

    /**
     * Translates a Claude `control_request {subtype:"can_use_tool"}` into a
     * `{type:"request", request:{type:"tool_permission_request"}}` sent to the webview,
     * storing the mapping so that when the webview responds we can write
     * the correct `control_response` back to Claude's stdin.
     *
     * Other control_request subtypes are forwarded as io_message for the webview to handle.
     */
    private fun bridgeControlRequest(channelId: String, msg: JsonObject) {
        val controlRequestId = msg["request_id"]?.jsonPrimitive?.contentOrNull ?: return
        val request = msg["request"]?.jsonObject ?: return
        val subtype = request["subtype"]?.jsonPrimitive?.contentOrNull ?: return

        if (subtype != "can_use_tool") {
            // Non-permission control_requests: forward as io_message so the webview can render them
            val inner = buildJsonObject {
                put("type", "io_message")
                put("channelId", channelId)
                put("message", msg)
                put("done", false)
            }
            sendToWebview(inner.toString())
            return
        }

        val toolName = request["tool_name"]?.jsonPrimitive?.contentOrNull ?: ""

        // For file-writing tools, the webview's Ne1 handler calls openDiff() which
        // requires a VS Code diff editor that Rider doesn't have. If it gets no diff
        // result it auto-rejects. Large file content also overflows JCEF's script limit.
        // Solution: auto-allow these tools here in Kotlin, bypassing the webview entirely.
        // The permission_mode setting already controls the overall trust level.
        if (toolName in setOf("Write", "Edit", "MultiEdit", "NotebookEdit")) {
            log.info("[channel/$channelId] auto-allowing $toolName (control_request_id=$controlRequestId)")
            writeControlResponse(channelId, controlRequestId,
                buildJsonObject { put("behavior", "allow") })
            return
        }

        log.info("[channel/$channelId] bridging $toolName permission to webview")

        val webviewRequestId = java.util.UUID.randomUUID().toString()

        val requestToWebview = buildJsonObject {
            put("type", "request")
            put("channelId", channelId)
            put("requestId", webviewRequestId)
            put("request", buildJsonObject {
                put("type", "tool_permission_request")
                put("toolName", JsonPrimitive(toolName))
                put("inputs", request["input"] ?: JsonNull)
                put("suggestions", request["permission_suggestions"] ?: JsonNull)
                put("toolUseId", request["tool_use_id"] ?: JsonPrimitive(""))
                // Pass through extra hint fields if present
                request["title"]?.let { put("title", it) }
                request["display_name"]?.let { put("displayName", it) }
                request["description"]?.let { put("description", it) }
                request["blocked_path"]?.let { put("blockedPath", it) }
                request["decision_reason"]?.let { put("decisionReason", it) }
            })
        }
        sendToWebview(requestToWebview.toString())
    }

    /**
     * Called by ClaudeMessageRouter when the webview sends a `{type:"response"}` for a
     * tool permission request.  Writes the corresponding `control_response` to Claude stdin.
     */
    fun handlePermissionResponse(requestId: String, response: JsonObject) {
        val (channelId, controlRequestId) = pendingPermissions.remove(requestId) ?: return
        val result = response["result"]?.jsonObject ?: buildJsonObject { put("behavior", "deny") }
        writeControlResponse(channelId, controlRequestId, result)
    }

    /**
     * Called when the webview sends `cancel_request` (user dismissed the permission dialog).
     * Sends a deny control_response so Claude doesn't hang.
     */
    fun cancelPermissionRequest(requestId: String) {
        val (channelId, controlRequestId) = pendingPermissions.remove(requestId) ?: return
        log.debug("Permission request $requestId cancelled by webview, sending deny to Claude")
        writeControlResponse(channelId, controlRequestId, buildJsonObject { put("behavior", "deny") })
    }

    private fun writeControlResponse(channelId: String, controlRequestId: String, result: JsonObject) {
        val channel = channels[channelId] ?: run {
            log.warn("writeControlResponse: channel $channelId not found")
            return
        }
        val msg = buildJsonObject {
            put("type", "control_response")
            put("response", buildJsonObject {
                put("subtype", "success")
                put("request_id", controlRequestId)
                put("response", result)
            })
        }
        try {
            val line = msg.toString() + "\n"
            channel.process.outputStream.write(line.toByteArray(Charsets.UTF_8))
            channel.process.outputStream.flush()
            log.info("writeControlResponse: channel=$channelId request_id=$controlRequestId behavior=${result["behavior"]}")
        } catch (e: Exception) {
            log.warn("Failed to write control_response for $controlRequestId: ${e.message}")
        }
    }

    private fun sendCloseChannel(channelId: String, error: String?) {
        val inner = buildJsonObject {
            put("type", "close_channel")
            put("channelId", channelId)
            if (error != null) put("error", error) else put("error", JsonNull)
        }
        sendToWebview(inner.toString())
    }

    private fun sendErrorToWebview(channelId: String, message: String) {
        sendCloseChannel(channelId, message)
    }

    private fun sendToWebview(innerJson: String) {
        ApplicationManager.getApplication().invokeLater {
            getBrowserManager()?.sendToWebview(innerJson)
        }
    }

    private fun getBrowserManager(): ClaudeBrowserManager? {
        val toolWindow = ToolWindowManager.getInstance(project).getToolWindow("Claude") ?: return null
        val panel = toolWindow.contentManager.contents
            .mapNotNull { it.component as? ClaudeToolWindowPanel }
            .firstOrNull() ?: return null
        return panel.browserManager
    }

    // ── Disposable ────────────────────────────────────────────────────────────

    override fun dispose() {
        scope.cancel()
        intentionallyClosed.addAll(channels.keys)
        channels.values.forEach { it.job.cancel(); it.process.destroy() }
        channels.clear()
    }
}
